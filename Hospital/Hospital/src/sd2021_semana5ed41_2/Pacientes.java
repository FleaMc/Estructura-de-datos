package sd2021_semana5ed41_2;

import java.util.Objects;

/**
 *
 * @author Michael
 */
public class Pacientes {
    //nombres, apellidos, cédula, edad, género
    private String nombre, apellido,genero;
    private int cedula,edad;

    public Pacientes(String nombre, String apellido, String genero, int cedula, int edad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.genero = genero;
        this.cedula = cedula;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public String getGenero() {
        return genero;
    }

    public int getCedula() {
        return cedula;
    }

    public int getEdad() {
        return edad;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Pacientes other = (Pacientes) obj;
        if (this.edad != other.edad) {
            return false;
        }
        if (!Objects.equals(this.nombre, other.nombre)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Pacientes{" + "nombre=" + nombre + ", apellido=" + apellido + ", genero=" + genero + ", cedula=" + cedula + ", edad=" + edad + '}';
    }
    
    
    
    
}
